/* ---MODULO OS--- */

const os = require ('os')

console.log(os.type()) // Esto nos muestra el sistema operativo que estamos usando en esta maquina.
console.log(os.homedir()) // Este te muestra en el directorio los usuarios y el usuario que se esta usando en ese momento.
console.log(os.uptime()) // Esto te mostrara cuanto tiempo se a estado usando el sistema operativo desde que se inicio.
console.log(os.userInfo()) // Esto te mostrara datos sobre tu usuario de windows.
